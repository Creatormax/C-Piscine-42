int	ft_is_prime(int nb)
{
	int	n;

	n = 3;
	if (nb <= 0 || nb == 1 || nb % 2 == 0)
		return (0);
	while (n < 46340 && nb >= n * n)
	{
		if (nb % n == 0)
			return (0);
		n += 2;
	}
	return (1);
}
